import java.util.*;
import javax.swing.*;

public class formatfdemo
{
  public static void main (String[] args)
  {
    System.out.println(1);
   
    System.out.println(11); 
    
    System.out.println(111);
    
    System.out.printf("%3d%n",  1);
    System.out.printf("%3d%n", 11);
    System.out.printf("%3d%n",111);

    System.out.printf("%15.2f%n",  1.);
     System.out.printf("%15.2f%n", 11.);
       System.out.printf("%15.2f%n",111.);


   System.exit(0);
  }
 }
public class AreaClass
{
   public static double getCalculateArea(double r)
   {
      return Math.PI * r * r;
   }
   
   public static double getCalculateArea(int l, int w)
   {
      return l * w;
   }
   
   public static double getCalculateArea(double r, double h)
   {
      return Math.PI * r * r * h;
   }
}